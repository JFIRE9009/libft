#include "libft.h"

char	*ft_strrev(char *s)
{
	int		len;
	int		i;
	char	c;

	i = 0;
	len = ft_strlen(s);
	while (i < len / 2)
	{
		c = s[i];
		s[i] = s[len];
		s[len] = c;
		i++;
		len--;
	}
	return (s);
}
